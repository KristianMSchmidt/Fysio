        <h2>Om 10-meter-gangtest</h2>

        <h4>Målgruppe</h4>
        <p>Testen er fundet egnet til måling af ganghastighed på tværs af patientkategorier. Faktorer der har betydning for måleresultatet kan være testpersonens udgangsposition, testpersonens fodtøj, prøve-forsøg, pauser, instruktion.</p>

        <h4>Tidsforbrug</h4>
        <p>Det tager ca. 5-10 minutter at udføre testen inkl. instruktion.</p>

        <h4>Hvad testes</h4>
        <p>10-meter gangtest måler den tid det tager for en person at gå 10 meter i sit normale gangtempo. Antal af skridt kan også tælles, hvis det er relevant. Der findes flere forskellige udgaver af 10-meter gangtest og det anbefales, at man benytter en 12 meter lang bane med fri gulvplads. Man beder patienten starte testen stående, så accelerationen ved testens begyndelse er en del af testen. Uret stoppes og testen bliver afsluttet, mens personen er i bevægelse.</p>

        <h5>Relaterede måsleredskaber: 6-minuttersgangtest, Shuttle Walk Test, Timed-Up-and-Go.</h5>
